////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "color.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Color::Color() : _r(0), _g(0), _b(0) {}

Color::Color(double r, double g, double b) : _r(r), _g(g), _b(b) {}

Color::Color(const Vector& vec) : _r(vec.getX()), _g(vec.getY()), _b(vec.getZ()) {}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

double Color::getR() const {
    return _r;
}

double Color::getG() const {
    return _g;
}

double Color::getB() const {
    return _b;
}

void Color::setColor(double r, double g, double b) {
    _r = r;
    _g = g;
    _b = b;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Color Color::operator*(const Color& color) {
    return Color(_r * color._r,
                 _g * color._g,
                 _b * color._b);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Color& Color::operator+=(const Color& color) {
    _r += color._r;
    _g += color._g;
    _b += color._b;
    return *this;
}

Color& Color::operator/=(double n) {
    _r /= n;
    _g /= n;
    _b /= n;
    return *this;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

std::ostream& operator<<(std::ostream& stream, const Color& color) {
    return stream << "(" << color._r << ", " << color._g << ", " << color._b << ")";
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Color Color::Red    = Color(1.0, 0.0, 0.0);
Color Color::Green  = Color(0.0, 1.0, 0.0);
Color Color::Blue   = Color(0.0, 0.0, 1.0);
Color Color::Purple = Color(1.0, 0.0, 1.0);
Color Color::Yellow = Color(1.0, 1.0, 0.0);
Color Color::White  = Color(1.0, 1.0, 1.0);
Color Color::Black  = Color(0.0, 0.0, 0.0);

