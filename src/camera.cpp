Camera::Camera(const Point& origin, const Point& screenCenter, const Vector& screenWidth, const Vector& screenHeight)
    : _origin(origin), _screenCenter(screenCenter), _screenWidth(screenWidth), _screenHeight(screenHeight), _bottom_left_corner() {
        _bottom_left_corner = _screenCenter - 0.5*(_screenWidth+_screenHeight);
    }

