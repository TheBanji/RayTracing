////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "skybox.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

SkyBox::SkyBox(const Color& color1, const Color& color2) : _color1(color1), _color2(color2) {}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Color SkyBox::operator()(double t) const {
    Color color;
    color.setColor(t*_color1.getR()+(1-t)*_color2.getR(), t*_color1.getG()+(1-t)*_color2.getG(), t*_color1.getB()+(1-t)*_color2.getB());
    return color;
}

