void seed_random(long long seed) {
    std::srand(seed);
}

double uniform_random() {
    #ifdef __linux__
    return (double)drand48();
    #elif __APPLE__
    return (double)drand48();
    #elif _WIN32
    return (double)std::rand() / MAX_INT;
    #endif
}

